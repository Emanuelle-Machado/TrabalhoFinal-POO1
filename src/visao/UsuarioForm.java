    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
         int linha = tabela.getSelectedRow();
         if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um usuário", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
         
        atualizaBotao(false);
        habilitarFormulario(true);
        
        usuarioSelecionado = usuarios.get(linha);
        txf_nome.setText(usuarioSelecionado.getNome());
        txf_login.setText(usuarioSelecionado.getLogin());
        pf_senha.setText(usuarioSelecionado.getSenha());
        cb_nivel.setSelectedItem(usuarioSelecionado.getNivel());
        
    }//GEN-LAST:event_btn_alterarActionPerformed

    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
       int linha = tabela.getSelectedRow();
        if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um usuário", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        usuarioSelecionado = usuarios.get(linha);
        try {
            usuarioDao.remover(usuarioSelecionado.getCodigo());
            JOptionPane.showMessageDialog(null, "Usuário excluído");
            atualizarTabela();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        limparTexto();
    }//GEN-LAST:event_btn_excluirActionPerformed

    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
        atualizaBotao(true);
        habilitarFormulario(false);

        String nome = txf_nome.getText();
        String login = txf_login.getText();
        String senha = pf_senha.getText();
        String nivel = (String) cb_nivel.getSelectedItem();

        if (nome.length() == 0) {
            JOptionPane.showMessageDialog(null, "informe um nome.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (login.length() == 0) {
            JOptionPane.showMessageDialog(null, "informe um login.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (senha.length() == 0) {
            JOptionPane.showMessageDialog(null, "informe uma senha.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (nivel.length() == 0) {
            JOptionPane.showMessageDialog(null, "informe um nivel.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (usuarioSelecionado == null) {

            Usuario usuario = new Usuario(nome, login, senha, nivel);

            try {
                usuarioDao.adicionar(usuario);
                JOptionPane.showMessageDialog(null, "usuário cadastrado com sucesso");
                
                limparTexto();
                
                txf_nome.requestFocus();

            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Erro ao inserir no bd", "Erro", JOptionPane.ERROR_MESSAGE);

            }

        } else {
            usuarioSelecionado.setNome(nome);
            usuarioSelecionado.setLogin(login);
            usuarioSelecionado.setSenha(senha);
            usuarioSelecionado.setNivel(nivel);

            try {

                usuarioDao.alterar(usuarioSelecionado);
                JOptionPane.showMessageDialog(null, "usuario alterado");
                atualizarTabela();

            } catch (SQLException ex) {
                ex.printStackTrace();
            }

        }
        
        usuarioSelecionado = null;    
        
        atualizarTabela();
    }//GEN-LAST:event_btn_salvarActionPerformed

