    private void atualizarTabela() {
        try {
            usuarios = usuarioDao.buscarPeloNome(txf_pesquisa.getText());

            DefaultTableModel model = (DefaultTableModel) tabela.getModel();
            model.setNumRows(0);
            for (int i = 0; i < usuarios.size(); i++) {
                Usuario usuario = usuarios.get(i);
                model.addRow(new Object[]{usuario.getNome(), usuario.getLogin(), usuario.getNivel(),});
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
