    private void btn_fecharPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_fecharPedidoActionPerformed
       
        //REGISTRAR O CLIENTE (NOME, TELEFONE,ENDERE√áO)
        int linha = tabela.getSelectedRow();
         if (linha < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um cliente", "Aten√ß√£o", JOptionPane.WARNING_MESSAGE);
            return;
        }
         clienteSelecionado = clientes.get(linha);
         
        //REGISTRAR O M√âTODO DE PAGAMENTO
        String pagamento = "";
        if(cb_pagamento == null){
            JOptionPane.showMessageDialog(null, "Selecione o m√©todo de pagamento", "Aten√ß√£o", JOptionPane.WARNING_MESSAGE);
        }else{
            if(cb_pagamento.getSelectedItem().equals("Cr√©dito")){
            pagamento = "Cr√©dito";
        }else if(cb_pagamento.getSelectedItem().equals("D√©bito")){
            pagamento = "D√©bito";
        }else if(cb_pagamento.getSelectedItem().equals("Boleto")){
            pagamento = "Boleto";
        }else if(cb_pagamento.getSelectedItem().equals("Pix")){
            pagamento = "Pix";
        }
        }
        
        //REGISTRANDO ESCOLHAS, CALCULANDO E REGISTRANDO O VALOR TOTAL
        String bolo = " ";
        String kilos = "";
        String topo = " Topo personalizado R$ 20,00 ";
        String confetes = " Adicional de confete e kit-kat R$ 10,00 ";
        String lascas = " Adicional de lascas de chocolate R$ 10,00 ";
        String brigadeiros = " Adicional de brigadeiro R$ 10,00 ";
        
        double total = 0;
        if(btn_doisA.isSelected()){
            total = total + 53.00;
            bolo = " Dois Amores R$ 53,00 ";
        }else if (btn_ouroBranco.isSelected()){
            total = total + 55.00;
            bolo = " Ouro Branco R$ 55,00 ";
        }else if(btn_leite.isSelected()){
            total = total + 58.00;
            bolo = " Leite Ninho R$ 58,00 ";
        }else if(btn_prestigio.isSelected()){
            total = total + 53.00 ;
            bolo = " Prest√≠gio R$ 58,00 ";
        }
        if(btn_1kg.isSelected()){
            total = total * 1;
            kilos = " Peso de 1 Kg ";
        }else if(btn_2kg.isSelected()){
            total = total * 2;
            kilos = " Peso de 2 Kg ";
        }else if(btn_2kge5.isSelected()){
            total = total * 2.5;
            kilos = " Peso de 2,5 Kg ";
        }
        
        String extra = "";
        if(check_topo.isSelected()){
            total = total + 20.00;
            extra = topo;
        }
        if(check_confetes.isSelected()){
            total = total + 10.00;
            extra = confetes;
        }
        if(check_lascas.isSelected()){
            total = total + 10.00;
            extra = lascas;
        }
        if(check_brigadeiros.isSelected()){
            total = total + 10.00;
            extra = brigadeiros;
        }
        
        String descricao = "" + bolo + "" + kilos + "" + extra + "";

        GerarNota nota = new GerarNota(total, pagamento, descricao, clienteSelecionado, user);
                nota.setVisible(true);
                this.dispose();
    }//GEN-LAST:event_btn_fecharPedidoActionPerformed

